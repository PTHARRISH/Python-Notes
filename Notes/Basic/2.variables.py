# Variables
# Variable is containers that store values.

# Python is not “statically typed” it is Dynamically typed :
#   It doesn’t know about the type of the variable until the code is run.
#   So declaration is of no use. What it does is,
#   It stores that value at some memory location and then binds that variable name to that memory container.
#   And makes the contents of the container accessible through that variable name. 
#   So the data type does not matter. As it will get to know the type of the value at run-time.


